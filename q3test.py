from q3 import square,sphere,cube

sq = square(2)
per1 = sq.peri()
area1 = sq.area()
print("perimeter of square = " ,per1)
print("area of square = " ,area1)

sp = sphere(3)
vol2 = sp.vol()
area2 = sp.area()
print("volume of sphere = " ,vol2)
print("area of sphere = " ,area2)

cu = cube(4)
per3 = cu.peri()
area3 = cu.area()
vol3 = cu.vol()
print("perimeter of cube = " ,per3)
print("area of cube = " ,area3)
print("volume of cube = " , vol3)
